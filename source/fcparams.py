def plotMap(
    XX,
    YY,
    ZZ,
    ax,
    norm,
    cmap=None,
    lo=None,
    hi=None,
    numlv=6,
    zscale="linear",
    levels=None,
):

    if lo is None:
        lo = np.nanmin(ZZ)
    if hi is None:
        hi = np.nanmax(ZZ)

    if zscale == "linear" and levels is None:
        levels = np.linspace(lo, hi, numlv)
    elif zscale == "log" and levels is None:
        levels = np.logspace(np.log10(lo), np.log10(hi), numlv)

    if XX.shape[0] >= 3 and XX.shape[1] >= 3:
        X = np.zeros((XX.shape[0] + 1, XX.shape[1] + 1))
        X[:-1, :-1] = XX
        X[-1, :-1] = XX[-1, :]
        X[:, -1] = 2 * X[:, -2] - X[:, -3]

        Y = np.zeros((YY.shape[0] + 1, YY.shape[1] + 1))
        Y[:-1, :-1] = YY
        Y[:-1, -1] = YY[:, -1]
        Y[-1, :] = 2 * Y[-2, :] - Y[-3, :]
    else:
        X = XX.copy()
        Y = YY.copy()

    if cmap is not None:
        CPfilled = ax.pcolormesh(X, Y, ZZ, norm=norm(vmin=lo, vmax=hi), cmap=cmap)
    else:
        CPfilled = ax.pcolormesh(X, Y, ZZ, norm=norm(vmin=lo, vmax=hi))

    return ax, CPfilled, levels
