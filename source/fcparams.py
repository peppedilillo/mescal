def changeLineCycle(rcParams, style=True, color="k", mpl=None):
    if style and color:
        if color == "k":
            colors = cycler("color", ["k", "r", "b", (0.2, 0.8, 0.2), "c", "m", "y"])
            if mpl is not None:
                defineColorMap(mpl)
                rcParams["image.cmap"] = "hot_ur"
            else:
                rcParams["image.cmap"] = "inferno_r"
                # rcParams['image.cmap'] = 'plasma_r'
        elif color == "b":
            colors = cycler("color", ["b", "r", (0.2, 0.8, 0.2), "c", "m", "y", "k"])
            rcParams["image.cmap"] = "jet"

        Ncolors = len(colors)
        styles = cycler(
            "linestyle",
            [
                "-",
                "--",
                ":",
                "-.",
                (0, (6.4, 1.6, 1, 1.6, 1, 1.6)),
                (0, (1, 1.6, 3.2, 1.6, 3.2, 1.6)),
            ],
        )
        Nstyles = len(styles)
        styles = styles.concat((Ncolors - 1) * styles)
        colors = colors.concat((Nstyles - 1) * colors)
        rcParams["axes.prop_cycle"] = styles + colors
    elif style and not color:
        rcParams["axes.prop_cycle"] = cycler(
            "linestyle",
            [
                "-",
                "--",
                ":",
                "-.",
                (0, (6.4, 1.6, 1, 1.6, 1, 1.6)),
                (0, (1, 1.6, 3.2, 1.6, 3.2, 1.6)),
            ],
        )
    elif not style and color:
        if color == "k":
            colors = cycler("color", ["k", "r", "b", (0.2, 0.8, 0.2), "c", "m", "y"])
            if mpl is not None:
                defineColorMap(mpl)
                rcParams["image.cmap"] = "hot_ur"
            else:
                rcParams["image.cmap"] = "inferno_r"
                # rcParams['image.cmap'] = 'plasma_r'
        elif color == "b":
            colors = cycler("color", ["b", "r", (0.2, 0.8, 0.2), "c", "m", "y", "k"])
        rcParams["axes.prop_cycle"] = colors
        rcParams["image.cmap"] = "jet"
    return rcParams


def newHatchCycle():
    return itertools.cycle(["//", "\\\\", "||", "--", "xx", "++"])


def changeMarkerCycle():
    return itertools.cycle(("o", "^", "s", "v", "D", "P"))


def newMarkerCycle(ver=1):
    if ver == 1:
        return itertools.cycle(("o", "^", "s", "v", "D", "P"))

    if ver == 2:
        return itertools.cycle(("o", "s", "x", "p", "^", "v"))


def newLineCycle(line="-"):
    if line == "-":
        return itertools.cycle(
            [
                "-",
                "--",
                ":",
                "-.",
                (0, (6.4, 1.6, 1, 1.6, 1, 1.6)),
                (0, (1, 1.6, 3.2, 1.6, 3.2, 1.6)),
            ]
        )
    if line == "--":
        return itertools.cycle(
            [
                "--",
                ":",
                "-.",
                (0, (6.4, 1.6, 1, 1.6, 1, 1.6)),
                (0, (1, 1.6, 3.2, 1.6, 3.2, 1.6)),
                "-",
            ]
        )
    if line == ":":
        return itertools.cycle(
            [
                ":",
                "-.",
                (0, (6.4, 1.6, 1, 1.6, 1, 1.6)),
                (0, (1, 1.6, 3.2, 1.6, 3.2, 1.6)),
                "-",
                "--",
            ]
        )


def newColorCycle(color="k"):
    if color == "k":
        return itertools.cycle(["k", "r", "b", (0.2, 0.8, 0.2), "c", "m", "y"])
    if color == "b":
        return itertools.cycle(["b", "r", (0.2, 0.8, 0.2), "c", "m", "y", "k"])
    if color == "r":
        return itertools.cycle(["r", "b", (0.2, 0.8, 0.2), "c", "m", "y", "k"])


